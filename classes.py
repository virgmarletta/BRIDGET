"""
========================================
BRIDGET AGENT GENERATION ENGINE
========================================

This module implements the human and AI agent's predictive pipelines.
It is structured as follows:

* User Base class: Models realistic human behaviour towards the AI, originally defined in the FRANK algorithm
[original code at: https://github.com/FedericoMz/Frank/tree/main]

* BetaUser subclass: Simulates user prediction mechanism modeled with the Instance-Dependent Label Noise (IDN) approach.
The mathematical formulation relies on the OpenL2D Synthetic Expert Generation Scheme 
(Alves, J. et al. (2025) A benchmarking framework and dataset for learning to defer in human-ai decision-making.
Source code available at: https://github.com/feedzai/openl2d/tree/main)

The formulation is revised hereby to adapt to the streaming learning context of BRIDGET's Human in Command phase.
Full reasoning and formal mathematical definition provided in:
https://drive.google.com/file/d/1J7ULywuyRuRCYHXtx1pa01Q_uasaheio/view?usp=sharing 

* DeferralNet: A 2-layer neural network classifier implemented in PyTorch, acting as the principal classifier
during the Machine in Command phase

* PyTorchWrapper: A custom wrapper compliant with sklearn's BaseEstimator for smooth deployment within the 
GrowingSpheres algorithm. Its definition is model agnostic.

"""


